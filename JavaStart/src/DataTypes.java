class DataTypes {
    public static void main(String[] args) {
        System.out.print("Cześć Jan, słyszałem, że masz 25 lat i uczysz się programowania. Czy to prawda?");
        System.out.print('\n');
        System.out.print(true);
        System.out.print('\n');

        System.out.print("Czy Twoje inicjały to J K? ");
        System.out.print('\n');
        System.out.print(false);
        System.out.print('\n');

        System.out.print("Liczba int:");
        System.out.print('\n');
        System.out.print("1985");
        System.out.print('\n');

        System.out.print("Znak char:");
        System.out.print('\n');
        System.out.print('\u2602');
        System.out.print('\n');

        System.out.print("Liczba double:");
        System.out.print('\n');
        System.out.print("99.666");
        System.out.print('\n');

        System.out.println("Liczba szatana:");
        System.out.print(999);
        System.out.print('\n');


        System.out.println("Mam Audi A4");
        System.out.println("Rocznik:");
        System.out.println(2004);
        System.out.println("Silnik o pojemności:");
        System.out.println(2.0);
    }
}
